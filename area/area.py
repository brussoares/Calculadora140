# Função calcular area de um quadrado
def calcular_area_de_um_quadrado(lado):
    areaQuadrado = lado * lado
    return areaQuadrado

# Função calcular area de um retangulo
def calcular_area_de_um_retangulo(comprimento, largura):
    areaRetangulo = comprimento * largura
    return areaRetangulo

# Função calcular area de um triangulo
def calcular_area_de_um_triangulo(base, altura):
    areaTriangulo = (base * altura) / 2
    return areaTriangulo